# ─── FIX #3: correct COM-offset formula ──────────────────────────────────────
#
#  pygame.transform.rotate(img, +angle_deg) is CCW in screen space (pygame convention).
#  In screen Y-down coords CCW by θ maps (vx,vy) → (vx·cosθ+vy·sinθ, −vx·sinθ+vy·cosθ).
#  This is identical to Pymunk local_to_world→to_pygame, so image and physics stay welded.

def _rotated_com_offset(com, img_w, img_h, angle_rad):
    """
    Returns (rx, ry) – screen-space offset of the COM pixel from the centre
    of the rotated image returned by pygame.transform.rotate(img, +angle_deg).
    Formula: CCW rotation in Y-down screen space.
    """
    cx = img_w / 2
    cy = img_h / 2
    dx = com[0] - cx
    dy = com[1] - cy

    # CW rotation in screen space (Y-down)
    cos_a = math.cos(angle_rad)
    sin_a = math.sin(angle_rad)
    rx =  dx * cos_a + dy * sin_a
    ry = -dx * sin_a + dy * cos_a

    return rx, ry


def _blit_tile(surface, img, com, angle_rad, screen_pos):
    """Blit a rotated tile image so that the COM pixel lands on screen_pos."""
    angle_deg = math.degrees(angle_rad)
    rotated   = pygame.transform.rotate(img, angle_deg)   # +angle_deg: CCW matches physics Y-flip

    rx, ry = _rotated_com_offset(com, img.get_width(), img.get_height(), angle_rad)
    rot_cx = rotated.get_width()  / 2
    rot_cy = rotated.get_height() / 2

    draw_x = screen_pos[0] - (rot_cx + rx)
    draw_y = screen_pos[1] - (rot_cy + ry)
    surface.blit(rotated, (draw_x, draw_y))


