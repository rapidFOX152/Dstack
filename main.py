def to_pygame(p):
    """Convert Pymunk world coords → Pygame screen coords (Y flip)."""
    if hasattr(p, "x"):
        return int(p.x), int(SCREEN_HEIGHT - p.y)
    return int(p[0]), int(SCREEN_HEIGHT - p[1])

