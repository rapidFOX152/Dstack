# ─── Fallback polygon helper ──────────────────────────────────────────────────

def random_convex_polygon(avg_radius=45, num_verts=8):
    angles = sorted(random.uniform(0, 2 * math.pi) for _ in range(num_verts))
    pts    = [(avg_radius * random.uniform(0.7, 1.3) * math.cos(a),
               avg_radius * random.uniform(0.7, 1.3) * math.sin(a))
              for a in angles]
    hull   = ConvexHull(pts)
    return [pts[i] for i in hull.vertices]

