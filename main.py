def _remove_black_bg(surface: pygame.Surface) -> pygame.Surface:
    """Convert near-black pixels (brightness < 30) to transparent at runtime."""
    try:
        arr = pygame.surfarray.array3d(surface)   # shape (W, H, 3)
        brightness = arr[:, :, 0].astype(np.int32) + arr[:, :, 1] + arr[:, :, 2]
        alpha_arr  = np.where(brightness < 30, 0, 255).astype(np.uint8)
        result = surface.copy().convert_alpha()
        alpha_surf = pygame.surfarray.map_array(result, alpha_arr[:, :, np.newaxis].repeat(3, axis=2))
        pygame.surfarray.pixels_alpha(result)[:] = alpha_arr
        return result
    except Exception:
        return surface.convert_alpha()


def _load_tiles() -> list:
    """Return list of tile dicts, or [] on failure."""
    tile_defs = []

    json_path = "assets/data/tiles.json"
    if not os.path.exists(json_path):
        print(f"tiles.json not found at {json_path!r}. Run setup_assets.py first.")
        return tile_defs

    try:
        with open(json_path) as f:
            raw = json.load(f)
    except Exception as e:
        print(f"Could not parse {json_path}: {e}")
        return tile_defs

    print(f"tiles.json: {len(raw)} entries")

    for item in raw:
        fname = item["image_file"]

        # Prefer pre-processed transparent image; fall back to original with runtime removal
        clean_path = os.path.join("assets/images/tiles_clean", fname)
        orig_path  = os.path.join("assets/images/tiles",       fname)

        if os.path.exists(clean_path):
            img_path = clean_path
        elif os.path.exists(orig_path):
            img_path = orig_path
        else:
            print(f"  Warning: {fname} not found – skipping")
            continue

        try:
            img_orig = pygame.image.load(img_path).convert_alpha()
        except Exception as e:
            print(f"  Warning: could not load {img_path}: {e} – skipping")
            continue

        # If image came from original (black bg), strip it
        if img_path == orig_path:
            img_orig = _remove_black_bg(img_orig)

        orig_w, orig_h = img_orig.get_size()
        scale = min(TARGET_TILE_SIZE / orig_w, TARGET_TILE_SIZE / orig_h, 1.0)

        if scale < 1.0:
            new_w = int(orig_w * scale)
            new_h = int(orig_h * scale)
            img = pygame.transform.smoothscale(img_orig, (new_w, new_h))
        else:
            img   = img_orig
            scale = 1.0
            new_w, new_h = orig_w, orig_h

        pivot_key = "center_of_mass" if "center_of_mass" in item else "pivot"
        com = [item[pivot_key][0] * scale, item[pivot_key][1] * scale]

        convex_parts = [
            [[x * scale, y * scale] for x, y in part]
            for part in item["convex_parts"]
        ]

        outer_contour = (
            [[v[0] * scale, v[1] * scale] for v in item["outer_contour"]]
            if "outer_contour" in item else None
        )

        area = item.get("area")
        if area:
            area *= scale * scale

        tile_defs.append({
            "image_file":   fname,
            "surface":      img,
            "convex_parts": convex_parts,
            "com":          com,
            "area":         area,
            "outer_contour": outer_contour,
            "image_size":   (new_w, new_h),
        })
        print(f"  Loaded {fname} ({new_w}×{new_h})")

    print(f"Loaded {len(tile_defs)} tiles.")
    return tile_defs


