def draw_gradient_rect(surf, col_top, col_bot, rect):
    for y in range(rect.top, rect.bottom):
        t = (y - rect.top) / rect.height
        r = int(col_top[0] * (1 - t) + col_bot[0] * t)
        g = int(col_top[1] * (1 - t) + col_bot[1] * t)
        b = int(col_top[2] * (1 - t) + col_bot[2] * t)
        pygame.draw.line(surf, (r, g, b), (rect.left, y), (rect.right, y))


def draw_rounded_rect(surf, color, rect, radius, border=0, border_color=None):
    x, y, w, h = rect
    radius = min(radius, w // 2, h // 2)
    s = pygame.Surface((w, h), pygame.SRCALPHA)
    pygame.draw.rect(s, color, (0, 0, w, h), border_radius=radius)
    if border > 0 and border_color:
        pygame.draw.rect(s, border_color, (0, 0, w, h), border, border_radius=radius)
    surf.blit(s, (x, y))


