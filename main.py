def draw_preview(surface, pdata, x, y, angle, player):
    if "surface" in pdata:
        _blit_tile(surface, pdata["surface"], pdata["com"], angle, (x, y))
    else:
        # Fallback polygon preview
        ca, sa = math.cos(angle), math.sin(angle)
        pts = [(x + vx * ca - vy * sa, y - (vx * sa + vy * ca))
               for vx, vy in pdata["vertices"]]
        if len(pts) >= 3:
            color = P1_MAIN + (180,) if player == 1 else P2_MAIN + (180,)
            s = pygame.Surface((SCREEN_WIDTH, SCREEN_HEIGHT), pygame.SRCALPHA)
